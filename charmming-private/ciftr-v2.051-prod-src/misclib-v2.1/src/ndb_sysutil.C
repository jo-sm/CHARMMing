void ndb_execute_command(char *cmdstring)
/*
 * Execute the command string under the csh interpreter and wait for completion
 */
{
  int status;
  pid_t pid;

  /*
   * Get a child process.
   */
  
  if ((pid = fork()) < 0) {
    ndb_log_message(NDB_MSG_ERR,(char*)"Error occurred during fork() %s",strerror(errno));
    exit(1);
  }
  
  /*
     * The child executes the code inside the if.
     */
  if (pid == 0) {
    execl("/bin/csh", "csh", "-f", "-c", cmdstring, (char *) 0 ) ;
    ndb_log_message(NDB_MSG_ERR,(char*)"Error occurred during execl() %s",strerror(errno));
    exit(1);
  }
  
  /*
   * The parent executes the wait.
   */
  while (wait(&status) != pid)
    /* empty */ ;
}


int lock_reg(int fd, int cmd, int type, off_t offset, int whence, off_t len)
{
	struct flock	lock;

	lock.l_type = type;		/* F_RDLCK, F_WRLCK, F_UNLCK */
	lock.l_start = offset;	/* byte offset, relative to l_whence */
	lock.l_whence = whence;	/* SEEK_SET, SEEK_CUR, SEEK_END */
	lock.l_len = len;		/* #bytes (0 means to EOF) */

	return( fcntl(fd, cmd, &lock) );
}


pid_t lock_test(int fd, int type, off_t offset, int whence, off_t len)
{
	struct flock	lock;

	lock.l_type = type;		/* F_RDLCK or F_WRLCK */
	lock.l_start = offset;	/* byte offset, relative to l_whence */
	lock.l_whence = whence;	/* SEEK_SET, SEEK_CUR, SEEK_END */
	lock.l_len = len;		/* #bytes (0 means to EOF) */

	if (fcntl(fd, F_GETLK, &lock) < 0)
		ndb_log_message(NDB_MSG_ERR,(char*)"fcntl error");

	if (lock.l_type == F_UNLCK)
		return(0);		/* false, region is not locked by another proc */
	return(lock.l_pid);	/* true, return pid of lock owner */
}


